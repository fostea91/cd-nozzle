     (((556*gm^2+1737*gm+3069)/1728)*y^5 - ...
     ((388*gm^2+(1161-384*nu)*gm+1881-1728*nu)/576)*y^3 + ...
     ((304*gm^2+(831-576*nu)*gm+1242-2160*nu+864*nu^2)/864)*y)*z + ...
     (((52*gm^2+51*gm+327)/192)*y^3 - ...
     ((52*gm^2+75*gm+279-288*nu)/192)*y)*z^2 - ...
     ((7*gm-3)/12)*y*z^3;
    
u = 1 + u1*expan + u2*expan^2 + u3*expan^3;
v = sqrt((gm+1)*expan*0.5)*(v1*expan + v2*expan^2 + v3*expan^3);

