     ((388*gm^2+(1161-384*nu)*gm+(1881-1728*nu))/2304)*y^4 + ...
     ((304*gm^2+(831-576*nu)*gm+(1242-2160*nu+864*nu^2))/1728)*y^2 - ...
     ((2708*gm^2+(7839-5760*nu)*gm+(14211-32832*nu+20736*nu^2))/82944) + ...
     (((52*gm^2+51*gm+327)/384)*y^4 - ...
     ((52*gm^2+75*gm+279-288*nu)/192)*y^2 + ...
     ((92*gm^2+180*gm+639-1080*nu+432*nu^2)/1152))*z + ...
     ((-(7*gm-3)/8)*y^2 + (((13-16*nu)*gm-(27-24*nu))/48))*z^2 + ...
     ((4*gm^2-57*gm+27)/144)*z^3;
    
