     ((3380*gm^2+(11391-3840*nu)*gm+15291-11520*nu)/13824)*y^5 + ...
     ((3424*gm^2+(11271-7200*nu)*gm+15228-22680*nu+6480*nu^2)/13824)*y^3 - ...
